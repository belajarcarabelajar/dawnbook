## Prosedur di Ruang Operasi: Langkah demi Langkah Menuju Kesembuhan

Selamat datang di "jantung" dari seluruh proses tonsilektomi. Bagi banyak orang, ruang operasi mungkin terasa misterius atau bahkan menakutkan. Namun, sebenarnya ruang operasi adalah lingkungan yang sangat terkendali, terorganisir, dan didesain khusus untuk keamanan pasien.

Bayangkan ruang operasi seperti sebuah **kokpit pesawat jet modern**. Setiap kru memiliki peran spesifik, setiap instrumen memiliki tempatnya, dan ada daftar periksa (checklist) ketat yang diikuti sebelum "lepas landas" (memulai prosedur).

## 1. Memasuki Ruang Operasi dan Persiapan Awal

Begitu kamu atau anakmu didorong masuk ke ruang operasi, hal pertama yang akan dirasakan adalah suhu ruangan yang sejuk dan pencahayaan yang sangat terang.

Perawat pertama-tama akan memasang sensor pada tubuh untuk memantau tanda-tanda vital secara langsung. Ini meliputi pemasangan stiker EKG di dada untuk memantau irama jantung, klip oksimetri nadi di jari untuk mengukur kadar oksigen \\( \text{O}_2 \\) dalam darah, serta manset di lengan untuk memantau tekanan darah secara otomatis setiap beberapa menit.

Sebelum tindakan dimulai, seluruh tim medis akan melakukan **Time-Out Bedah**. Prosedur keselamatan standar ini memastikan identitas pasien, jenis operasi, dan lokasi pembedahan (dalam hal ini, kedua amandel) sudah benar.

## 2. Induksi Anestesi: "Tidur yang Nyenyak"

Tonsilektomi hampir selalu dilakukan di bawah **Anestesi Umum**. Ini berarti pasien akan tertidur sepenuhnya dan tidak merasakan apa pun selama prosedur berlangsung.

Dokter anestesi akan memulai proses induksi dengan memberikan obat tidur melalui infus (IV) atau masker pernapasan beraroma manis yang ramah anak. Setelah kamu tertidur lelap, dokter memasang selang pernapasan (*endotracheal tube*) agar jalan napas tetap aman dan terbuka selama dokter bedah bekerja di area tenggorokan.

> **Analogi:** Jika tubuh adalah sebuah rumah, anestesi adalah sistem keamanan yang mematikan lampu dan membungkam alarm untuk sementara. Dengan begitu, "tim renovasi" (dokter bedah) bisa bekerja tanpa gangguan atau menimbulkan rasa sakit bagi pemilik rumah.

## 3. Posisi dan Akses Bedah

Setelah pasien teranestesi dengan stabil, tim bedah akan mengatur posisi pasien untuk akses optimal ke tenggorokan.

1. **Posisi Rose:** Pasien dibaringkan terlentang dengan bahu sedikit diganjal agar leher sedikit menengadah (ekstensi).
2. **Pemasangan Mouth Gag (Pembuka Mulut):** Karena mulut harus tetap terbuka lebar, alat khusus bernama *McIvor* atau *Crowe-Davis mouth gag* dipasang. Alat ini menahan lidah ke bawah dan menjaga mulut tetap terbuka tanpa merusak gigi atau gusi.
3. **Penerangan:** Dokter bedah akan menggunakan lampu kepala (*headlamp*) yang sangat terang untuk melihat jauh ke dalam orofaring (bagian belakang tenggorokan).

## 4. Proses Pengangkatan (Diseksi Tonsil)

Inilah inti dari prosedur ini. Amandel terletak di dalam "kantong" yang disebut kapsul tonsil.

Dokter bedah akan memegang tonsil menggunakan klem, menariknya perlahan ke arah tengah, lalu memisahkannya dari jaringan otot di bawahnya dengan mengikuti garis kapsul tonsil. Proses ini membutuhkan ketelitian tinggi agar tonsil terangkat utuh tanpa mencederai otot tenggorokan yang berfungsi untuk menelan.

Tonsilektomi sebenarnya adalah salah satu operasi tertua di dunia. Bedanya, teknologi medis saat ini memungkinkan penggunaan alat presisi tinggi untuk meminimalkan kerusakan jaringan.

## 5. Hemostasis: Menghentikan Pendarahan

Area amandel kaya akan pembuluh darah kecil. Oleh karena itu, langkah paling penting setelah amandel diangkat adalah **Hemostasis** (penghentian pendarahan).

Karena area sekitar amandel dipenuhi banyak pembuluh darah kecil, dokter akan menghentikan pendarahan segera setelah amandel terangkat. Ada beberapa cara yang biasa digunakan, seperti memakai elektrokauter untuk menutup ujung pembuluh darah dengan panas listrik terkendali, mengikat pembuluh darah dengan benang bedah (ligasi), atau menekan area luka dengan kasa steril selama beberapa menit.

## 6. Penyelesaian dan Pemulihan Awal

Setelah kedua amandel diangkat dan area bedah dipastikan kering, dokter bedah melakukan langkah penutup berikut:

- **Pembersihan:** Sisa cairan atau darah dari tenggorokan dibersihkan secara menyeluruh menggunakan alat hisap khusus.
- **Pelepasan Alat:** Pembuka mulut dilepaskan secara perlahan dan hati-hati.
- **Ekstubasi:** Dokter anestesi mulai mengurangi dosis obat tidur, dan selang pernapasan akan dicabut begitu kamu bisa kembali bernapas sendiri secara stabil.

## Real-World Application: Skenario di Ruang Operasi

Mari kita lihat contoh nyata bagaimana tim bekerja secara sinkron:

**Skenario:** Seorang anak berusia 7 tahun sedang menjalani tonsilektomi karena *Sleep Apnea* (henti napas saat tidur).

- **Menit ke-0:** Tim melakukan *Time-Out*. "Pasien A, usia 7 tahun, rencana tonsilektomi bilateral."
- **Menit ke-5:** Dokter Anestesi memberikan gas melalui masker bergambar karakter kartun. Pasien tertidur dalam hitungan detik.
- **Menit ke-15:** Dokter Bedah telah memasang *mouth gag*. Dengan bantuan elektrokauter, tonsil kanan dipisahkan dari kapsulnya dalam waktu kurang dari 5 menit. Hampir tidak ada darah yang keluar.
- **Menit ke-25:** Kedua tonsil telah diangkat. Dokter Bedah memeriksa area "bed" (bekas tempat tonsil) dan tidak menemukan rembesan darah.
- **Menit ke-30:** Pasien mulai menggerakkan jari tangan, tanda mulai terbangun. Ia kemudian dipindahkan ke ruang pemulihan dalam kondisi stabil.

## Ringkasan Langkah Operasi

| Tahapan | Apa yang Terjadi? | Durasi Perkiraan |
| --- | --- | --- |
| **Persiapan** | Monitor dipasang, verifikasi data. | 5-10 menit |
| **Induksi** | Pasien dibuat tertidur (Anestesi). | 5-10 menit |
| **Operasi** | Pengangkatan tonsil dan kontrol pendarahan. | 20-40 menit |
| **Emergence** | Pasien dibangunkan dan selang napas dilepas. | 10 menit |

**Penting:** Seluruh prosedur ini biasanya memakan waktu total sekitar **45 hingga 60 menit** dari saat kamu masuk hingga keluar ruang operasi. Kecepatan bukanlah tujuan utama; ketelitian dan penghentian pendarahan yang sempurna adalah prioritas tertinggi tim medis.

*Setelah tahap ini selesai, perjalananmu berlanjut ke ruang pemulihan (PACU), yang akan dibahas secara detail pada bagian selanjutnya dari materi ini.*


