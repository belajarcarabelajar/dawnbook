## Tanda Bahaya dan Kapan Menghubungi Dokter

Meskipun tonsilektomi tergolong prosedur rutin yang aman, pemulihan di rumah tetap membutuhkan perhatian cermat. Bayangkan seperti merenovasi lorong yang sempit: tenggorokanmu sedang memulihkan diri, dan sewaktu-waktu bisa muncul kendala kecil yang butuh penanganan medis. Oleh karena itu, penting untuk mengenali perbedaan antara rasa tidak nyaman yang wajar dan tanda bahaya (*red flags*) yang memerlukan tindakan darurat.

## 1. Pendarahan Pasca-Operasi: Prioritas Utama

Pendarahan merupakan risiko yang paling diwaspadai setelah operasi amandel. Secara medis, pendarahan ini terbagi dalam dua fase waktu:
- **Pendarahan primer**, yang terjadi dalam 24 jam pertama pasca-operasi.
- **Pendarahan sekunder**, terjadi antara hari ke-5 hingga ke-10, tepat saat keropeng putih pelindung luka (*eschar*) mulai terkelupas.

### Kapan Harus Segera ke Unit Gawat Darurat (UGD)?

Segera pergi ke UGD jika mendapati tanda-tanda berikut:
- **Keluarnya darah merah segar**: Kamu atau anakmu meludahkan atau batuk mengeluarkan darah merah terang yang mengalir aktif.
- **Muntah berwarna gelap seperti bubuk kopi** (vomitus *coffee grounds*). Ini menandakan adanya darah lama yang bercampur dengan asam lambung.
- Pendarahan terus berlangsung lebih dari beberapa menit dan tidak kunjung mereda meskipun sudah mencoba berkumur dengan air es.

> **Analogi Sederhana:** Bayangkan luka operasi ini seperti luka lecet di lutut yang sedang mengering. Ketika mulai sembuh, terbentuk lapisan putih di tenggorokan. Jika lapisan ini terkelupas terlalu cepat karena gesekan makanan keras atau kurang minum, pembuluh darah di bawahnya bisa terbuka kembali dan memicu pendarahan.

## 2. Dehidrasi: Musuh Tersembunyi

Rasa sakit saat menelan sering kali membuat orang enggan minum. Padahal, kekurangan cairan justru memperlambat pemulihan jaringan tenggorokan dan meningkatkan risiko pendarahan sekunder.

**Tanda-tanda dehidrasi yang harus kamu waspadai:**
- Buang air kecil sangat jarang, bahkan tidak buang air kecil sama sekali selama lebih dari 8-12 jam.
- **Urine berwarna kuning pekat** atau cenderung jingga gelap.
- Bibir tampak pecah-pecah, mulut sangat kering, dan anak-anak menangis tanpa mengeluarkan air mata.
- Tubuh terasa lemas luar biasa, disertai rasa pusing saat mencoba berdiri.

Untuk mencegahnya, kamu bisa mencatat jumlah cairan yang masuk setiap jam. Usahakan untuk minum setidaknya \\(30\text{-}50\text{ ml}\\) cairan setiap jam selama tidak tidur.

## 3. Masalah Pernapasan dan Suara

Sedikit pembengkakan di area tenggorokan adalah hal yang wajar. Namun, jika pembengkakan tersebut mulai menyempitkan jalan napas, kondisi ini harus segera ditangani.

**Segera hubungi dokter jika menemukan gejala berikut:**
- **Stridor**, yaitu suara napas berbunyi tinggi atau mencicit saat menarik napas.
- Napas terasa sesak atau dada dan leher tampak naik-turun secara berlebihan untuk membantu pernapasan.
- Air liur mengalir keluar terus-menerus (*drooling*) karena tenggorokan sama sekali tidak bisa digunakan untuk menelan.

## 4. Demam dan Infeksi

Suhu tubuh yang sedikit meningkat (sekitar \\(37.5^\circ\text{C} - 38^\circ\text{C}\\)) biasa terjadi pada hari pertama atau kedua. Kamu perlu waspada jika muncul tanda-tanda infeksi berikut:
- Demam tinggi dengan suhu tubuh mencapai lebih dari \\(38.5^\circ\text{C}\\) dan tidak kunjung turun walau sudah minum obat pereda demam.
- Nyeri hebat yang justru bertambah parah setelah hari ketujuh, bukannya berangsur membaik.
- Bau mulut yang sangat menyengat (*fetor oris*) disertai dengan keluarnya nanah pada area bekas operasi.

## Skenario Tanggap Darurat

Berikut adalah contoh perbedaan penanganan medis yang cepat dibandingkan dengan penundaan tindakan:

**Skenario A (Tindakan Cepat dan Tepat):** Rudi (8 tahun) terbangun pada hari keenam pasca-operasi dengan bercak darah segar di bantal, lalu meludahkan sedikit darah merah. Ibunya segera meminta Rudi berkumur dengan air es. Karena pendarahan tidak berhenti setelah lima menit, ibunya langsung membawa Rudi ke UGD. *Hasil: Dokter dapat menghentikan pendarahan kecil tersebut dengan prosedur sederhana sebelum berkembang menjadi pendarahan serius.*

**Skenario B (Tindakan Terlambat):** Santi merasa sangat lemas dan tidak buang air kecil sejak pagi karena takut merasakan nyeri saat menelan. Ia mengira kondisi lemas ini wajar setelah operasi. Sore harinya, Santi pingsan akibat dehidrasi berat. *Hasil: Santi harus menjalani rawat inap kembali untuk mendapatkan infus cairan intravena, yang sebenarnya bisa dicegah jika tanda dehidrasi diantisipasi lebih awal.*

## Ringkasan Tindakan Darurat

| Gejala | Tindakan |
| --- | --- |
| Pendarahan darah merah segar yang terus mengalir | **Segera ke UGD** |
| Sesak napas atau napas berbunyi mencicit | **Segera ke UGD** |
| Demam tinggi lebih dari \\(38.5^\circ\text{C}\\) | Hubungi dokter bedah atau klinik |
| Tidak buang air kecil lebih dari 8 jam | Hubungi dokter bedah atau klinik |
| Nyeri hebat yang tidak membaik dengan obat pereda nyeri | Hubungi dokter bedah atau klinik |

> [!IMPORTANT]
> Lebih baik berkonsultasi ke klinik atau pergi ke rumah sakit meskipun ternyata tidak ada masalah serius, daripada mengabaikan gejala yang bisa membahayakan keselamatan. Jika instingmu merasa ada sesuatu yang tidak beres, segera hubungi tenaga medis.

Sebagai langkah antisipasi, simpan nomor darurat dokter atau rumah sakit tempat operasi dilakukan pada kontak cepat di ponselmu agar mudah dihubungi saat dibutuhkan.


