## Ringkasan Persiapan

> **Pesan Penting:** Menjalankan persiapan dengan cermat membantu kamu mematuhi petunjuk dokter sekaligus mendatangkan ketenangan pikiran. Kondisi fisik yang prima dan kondisi psikologis yang stabil akan mempermudah jalannya prosedur bedah tonsilektomi.

**Daftar Periksa Terakhirmu:**

1. Hasil lab darah lengkap dan koagulasi sudah diserahkan ke pihak rumah sakit.
2. Sesi tanya jawab dengan dokter anestesi telah selesai dilakukan.
3. Semua suplemen dan obat pengencer darah sudah dihentikan sesuai batas waktu.
4. Jam mulai puasa makanan padat dan cairan jernih sudah dicatat baik-baik.
5. Seseorang yang tepercaya sudah dikonfirmasi akan mendampingi perjalanan pulang setelah operasi.

*Setelah semua persiapan ini selesai, kamu kini siap melangkah ke tahap berikutnya: prosedur di ruang operasi.*


