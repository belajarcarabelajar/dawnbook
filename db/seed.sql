## Alasan Medis dan Indikasi Operasi: Kapan Amandel Harus Diangkat?

Banyak orang menduga bahwa radang atau sakit tenggorokan biasa sudah cukup menjadi alasan untuk menjalani tonsilektomi. Padahal, dalam dunia medis modern, keputusan untuk melakukan operasi tidak diambil secara sembarangan.

Bayangkan amandelmu sebagai "satpam" di gerbang tenggorokan. Jika satpam tersebut sesekali tertidur atau kewalahan, kita mungkin bisa memaafkannya. Namun, jika satpam tersebut justru menjadi sumber keributan atau menyumbat jalur napas utama, maka sudah saatnya mencari solusi permanen.

Mari kita ulas berbagai kondisi kesehatan yang menjadi alasan kuat bagi dokter spesialis THT (Telinga, Hidung, Tenggorokan) untuk merekomendasikan tindakan ini.

## 1. Tonsilitis Rekuren (Infeksi Berulang)

Alasan paling umum untuk tindakan tonsilektomi adalah infeksi yang terjadi terus-menerus. Gejala ini tidak sekadar muncul sesekali dalam setahun, tetapi frekuensinya sudah sangat mengganggu aktivitas sehari-hari, sekolah, hingga pekerjaan.

### Kriteria Paradise

Dunia medis menggunakan standar bernama **Kriteria Paradise** untuk menentukan kelayakan operasi berdasarkan frekuensi infeksi. Pembedahan biasanya dipertimbangkan jika pasien mengalami kondisi berikut:

- Terjadi minimal \\(\ge 7\\) episode infeksi dalam kurun waktu satu tahun terakhir.
- Setidaknya \\(\ge 5\\) episode per tahun selama dua tahun berturut-turut.
- Minimal \\(\ge 3\\) episode per tahun selama tiga tahun berturut-turut.

**Catatan Klinis:** Setiap episode infeksi di atas harus disertai dengan minimal salah satu gejala klinis berikut:

1. Demam dengan suhu tubuh terukur melebihi \\(38,3^\circ\text{C}\\).
2. Terjadi pembengkakan kelenjar getah bening di area leher.
3. Muncul bercak putih atau eksudat pada permukaan amandel.
4. Hasil tes usap (swab) tenggorokan positif menunjukkan infeksi bakteri *Group A Beta-Hemolytic Streptococcus* (GABHS).

Sebagai pertimbangan praktis, jika anak terpaksa absen sekolah hingga 20 hari dalam setahun karena radang amandel berulang, dampak ketertinggalan pelajaran bisa berdampak lebih buruk bagi perkembangannya dibandingkan dengan risiko dari prosedur operasi itu sendiri.

## 2. Hipertrofi Tonsil (Pembengkakan Berlebih)

**Hipertrofi** merupakan istilah medis yang merujuk pada pembesaran suatu organ. Pada kasus amandel, pembengkakan yang berlebihan dapat menyumbat jalan napas atau mengganggu jalur makanan.

Dokter biasanya menilai ukuran pembesaran ini menggunakan standar **Skala Brodsky** mulai dari \\(0\\) hingga \\(+4\\):

- **Grade 0:** Posisi amandel sepenuhnya tersembunyi di dalam kantung atau fossa tonsil.
- **Grade +1:** Pembesaran amandel yang mengisi kurang dari \\(<25\%\\) ruang tenggorokan.
- **Grade +2:** Amandel membengkak dan menempati sekitar \\(25\%\\) hingga \\(50\%\\) rongga tenggorokan.
- **Grade +3:** Ukuran amandel menutupi sebagian besar jalan napas, berkisar antara \\(50\%\\) sampai \\(75\%\\).
- **Grade +4:** Penyumbatan parah dengan amandel mengisi lebih dari \\(>75\%\\) ruang tenggorokan. Kondisi ini sering dijuluki *"Kissing Tonsils"* karena amandel kiri dan kanan saling menempel.

### Dampak Hipertrofi

- **Henti napas saat tidur (Sleep Apnea):** Jalur napas tersumbat oleh amandel yang terlalu besar, membuat pasien sempat berhenti bernapas selama beberapa saat ketika tidur.
- **Kesulitan menelan (Disfagia):** Makanan padat menjadi sulit melewati tenggorokan karena ruang yang menyempit.
- **Perubahan pada karakter suara:** Suara terdengar tidak jelas, seperti sedang mengulum kentang panas (*hot potato voice*).

## 3. Gangguan Tidur dan OSA (Obstructive Sleep Apnea)

Indikasi ini semakin sering dijumpai pada pasien anak-anak. Jika pembengkakan amandel memicu terjadinya **Obstructive Sleep Apnea (OSA)** atau henti napas saat tidur, pembedahan biasanya menjadi rekomendasi utama.

**Gejala OSA yang perlu diwaspadai:**

1. Suara mendengkur terdengar sangat keras dan tidak wajar.
2. Napas sempat terhenti sejenak, lalu pasien tiba-tiba tersedak atau megap-megap saat tidur.
3. Posisi tidur terlihat tidak biasa, misalnya kepala mendongak jauh ke belakang demi membantu membuka jalan napas yang tersumbat.
4. Kembali mengompol pada anak yang sebenarnya sudah bisa mengontrol buang air kecil saat tidur.
5. Di siang hari, pasien terlihat sangat mengantuk, sulit berkonsentrasi, atau justru menunjukkan perilaku hiperaktif.

Secara sederhana, kondisi ini mirip seperti bernapas lewat sedotan kecil sepanjang malam. Jantung dan otak dipaksa bekerja ekstra keras untuk memenuhi kebutuhan oksigen tubuh. Tindakan operasi dilakukan untuk membebaskan jalan napas agar pasokan oksigen kembali normal.

## 4. Komplikasi dan Kondisi Medis Spesifik Lainnya

Selain faktor infeksi dan ukuran amandel, ada beberapa kondisi mendesak lain yang memerlukan tindakan bedah:

- **Abses Peritonsilar (Quinsy):** Kondisi darurat berupa penumpukan nanah di belakang jaringan amandel. Jika infeksi ini terus berulang atau menyumbat jalan napas secara mendadak, operasi pengangkatan amandel harus segera dilakukan.
- **Kecurigaan Kanker (Keganasan):** Saat ukuran salah satu amandel membesar sebelah secara drastis (asimetris) tanpa adanya tanda infeksi. Dokter akan menyarankan biopsi lewat operasi pengangkatan untuk menepis kemungkinan tumor ganas.
- **Bau mulut kronis (Halitosis):** Terjadi karena sisa makanan tersangkut di celah atau lubang amandel (kripta) dan mengeras menjadi batu amandel (*tonsilloliths*). Jika bau tajam ini terus bertahan dan tidak membaik dengan pembersihan biasa, tonsilektomi bisa menjadi solusi akhir.
- **Sindrom PFAPA:** Singkatan dari *Periodic Fever, Aphthous Stomatitis, Pharyngitis, and Adenitis*. Ini merupakan gangguan langka pada anak yang ditandai dengan demam tinggi berulang setiap beberapa minggu secara berkala.

## Real-World Application: Skenario Kasus

Mari kita ulas contoh kasus nyata agar kamu bisa melihat bagaimana kriteria di atas diterapkan:

**Kasus A: Budi (7 tahun)**  
Budi mendengkur keras setiap malam saat tidur. Ibunya menyadari bahwa napas Budi kerap kali tampak berhenti selama beberapa detik sebelum akhirnya ia tersentak bangun. Guru di sekolah juga mengeluhkan bahwa Budi sulit berkonsentrasi dan sering mengantuk di kelas. Berdasarkan pemeriksaan medis, amandel Budi membengkak hingga **Grade +4**.

- **Keputusan:** Tindakan operasi sangat direkomendasikan karena kondisi **Obstructive Sleep Apnea (OSA)** yang dialaminya berpotensi menghambat tumbuh kembang fisik dan mental.

**Kasus B: Siska (25 tahun)**  
Siska sempat mengeluhkan sakit tenggorokan sebanyak dua kali sepanjang tahun lalu. Terkadang ia merasa amandelnya mengganjal, tetapi ia tidak pernah mengalami demam tinggi atau kesulitan bernapas saat tidur.

- **Keputusan:** Prosedur pembedahan **tidak disarankan** karena gejala tersebut belum memenuhi kriteria medis yang kuat, sehingga risiko operasi dianggap melebihi potensi manfaatnya.

