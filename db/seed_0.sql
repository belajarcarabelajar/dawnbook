# Logika Berpikir Anak (Tahap Praoperasional)
def cek_volume(wadah_A, wadah_C):
    # Anak hanya melakukan centration (fokus pada tinggi)
    if wadah_C.tinggi > wadah_A.tinggi:
        return "Gelas C punya lebih banyak air!"
    else:
        return "Sama saja."

